/* Evento de clique nos botões da página inicial */
function clique() {
    window.location = 'erro.html';
}

/* Evento de clique na página de erro */
function back() {
    window.location = 'home.html';
}

/* Eventos de clique nas redes sociais */

function insta() {
    window.open('https://www.instagram.com/', '_blank');
}

function twitter() {
    window.open('https://twitter.com/', '_blank');
}

function face() {
    window.open('https://www.facebook.com/', '_blank');
}